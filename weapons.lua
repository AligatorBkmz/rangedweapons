function rangedweapons.get_gun_damage(gun_record)
 --local damage = gun_record.critical_damage / gun_record.critical_chance * gun_record.rate_of_fire
  local damage =((1 + gun_record.critical_chance * (gun_record.critical_damage - 1)) * gun_record.rate_of_fire * gun_record.bullet_velocity)/2
 return damage
end
function rangedweapons.get_ammo_list()
 local ammoTypes = {}
 for gunName, gunData in pairs(rangedweapons.guns) do
   table.insert(ammoTypes, gunData.Ammunition)
 end

 -- Remove duplicates by converting the table to a set and back to a table
 local uniqueAmmoTypes = {}
 local set = {}
 for _, ammoType in ipairs(ammoTypes) do
  if not set[ammoType] then
    table.insert(uniqueAmmoTypes, ammoType)
    set[ammoType] = true
  end
 end

 -- Concatenate all unique Ammunition types into a comma-separated string
 --local allAmmoTypes = table.concat(uniqueAmmoTypes, ", ")
 return uniqueAmmoTypes
end
